import UIKit

extension UIColor {
    
    static var lightThemeTint: UIColor {
        return UIColor(red: 251/255, green: 255/255, blue: 231/255, alpha: 1.0)
    }
    
    static var darkThemeTint: UIColor {
        return UIColor(red: 65/255, green: 65/255, blue: 65/255, alpha: 1.0)
    }
}
